'''
Question:
Tumhare paas ek list hai: numbers = [10, 20, 30, 40, 50]. 
Tumhe list ke elements ko reverse order mein print karna hai.
'''


numbers = [10, 20, 30, 40, 50]

for number in reversed(numbers):
    print(number)


